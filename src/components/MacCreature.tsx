export type FaceKey = keyof typeof FACES;

const ROTATIONS: Record<"idle" | "focus" | "break", FaceKey[]> = {
  idle:  ["happy", "smile", "happy", "bigSmile", "neutral", "happy"],
  focus: ["neutral", "angry", "neutral", "smile", "angry", "happy"],
  break: ["excited", "bigSmile", "excited", "happy", "bigSmile", "surprised"],
};

interface Props {
  mode: "idle" | "focus" | "break";
  size?: number;     // px wide; height auto-derived from body grid
}

const FACE_X = 4, FACE_Y = 4;

export function MacCreature({ mode, size = 240 }: Props) {
  const cols = MAC_BODY[0].length;
  const rows = MAC_BODY.length;
  const px = size / cols;

  const [idx, setIdx] = useState(0);
  const [blink, setBlink] = useState(false);

  // pose rotation: switch every ~2.5s
  useEffect(() => {
    const id = setInterval(() => setIdx((i) => i + 1), 2500);
    return () => clearInterval(id);
  }, []);
  // blinks
  useEffect(() => {
    let t: NodeJS.Timeout;
    const sched = () => {
      const wait = 3500 + Math.random() * 3000;
      t = setTimeout(() => {
        setBlink(true);
        setTimeout(() => { setBlink(false); sched(); }, 180);
      }, wait);
    };
    sched();
    return () => clearTimeout(t);
  }, []);

  const face: FaceKey = useMemo(() => {
    if (blink) return "sleepy";
    const list = ROTATIONS[mode];
    return list[idx % list.length];
  }, [idx, blink, mode]);

  const cells: { x: number; y: number; ink: boolean }[] = useMemo(() => {
    const out: { x: number; y: number; ink: boolean }[] = [];
    for (let y = 0; y < rows; y++) {
      const row = MAC_BODY[y];
      for (let x = 0; x < cols; x++) {
        const ch = row[x];
        if (ch === "#") out.push({ x, y, ink: true });
        else if (ch === ".") out.push({ x, y, ink: false });
      }
    }
    const overlay = FACES[face];
    for (let fy = 0; fy < overlay.length; fy++) {
      const row = overlay[fy];
      for (let fx = 0; fx < row.length; fx++) {
        if (row[fx] === "#") {
          out.push({ x: FACE_X + fx, y: FACE_Y + fy, ink: true });
        }
      }
    }
    return out;
  }, [face]);

  return (
    <svg
      width={size}
      height={Math.round(rows * px)}
      viewBox={`0 0 ${cols} ${rows}`}
      shapeRendering="crispEdges"
      style={{ imageRendering: "pixelated" }}
      aria-label={`Macintosh creature - ${face}`}
    >
      {cells.map((c, i) => (
        <rect key={i} x={c.x} y={c.y} width={1} height={1}
          fill={c.ink ? "#0a0a0a" : "#f5f5f0"} />
      ))}
    </svg>
  );
}
